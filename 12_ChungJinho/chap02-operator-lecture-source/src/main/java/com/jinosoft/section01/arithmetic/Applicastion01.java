package com.jinosoft.section01.arithmetic;

public class Applicastion01 {
  public static void main(String[] args) {
    int num1 = 20;
    int num2 = 7;
    System.out.println("num1 + num2 = " + (num1 + num2));
    System.out.println("num1 - num2 = " + (num1 - num2));
    System.out.println("num1 * num2 = " + (num1 * num2));
    System.out.println("num1 / num2 = " + (num1 / num2));
    System.out.println("num1 % num2 = " + (num1 % num2));

  }
}
